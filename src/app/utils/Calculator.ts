const add = (a: Number, b:Number):Number => Number(a) + Number(b);

const sub = (a: Number, b:Number):Number => Number(a) - Number(b);

const multiply = (a: Number, b:Number):Number => Number(a) * Number(b);

const divide = (a: Number, b:Number):Number => Number(a) / Number(b);

export {
  add,
  sub,
  multiply,
  divide
}
